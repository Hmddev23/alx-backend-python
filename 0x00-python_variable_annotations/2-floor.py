#!/usr/bin/env python3
"""
provide mathematical functions.
"""

import math

def floor(n: float) -> int:
    """
    return the largest integer less than or equal to a given number.
    
    Parameters:
        n (float): A floating-point number.
    
    Returns:
        int: The largest integer less than or equal to the input number.
    """
    return math.floor(n)
